                                From the Foundation’s inception, its approach has been to create opportunities to earn a
                                basic income and to provide needed resources for a better future.  The Foundation does
                                not merely give money to individuals. Instead, it helps create a thriving community and
                                family life by paying for the education of otherwise helpless children grades K through
                                6,
                                and ensuring that all related services, such as food and health care, are secured
                                through
                                local vendors.

                            </p>
                            <p class="text mt-3 mt-mb-0 wow fadeInUp" data-wow-delay=".5s">
                                For nearly a decade, the Foundation has built schools, offered basic healthcare, and
                                provided economic opportunities, including employment in teaching, medical services,
                                construction and related skills.  By sourcing supplies locally whenever possible, the
                                Foundation has helped to build businesses in the community.

                            </p>
                            <p class="text mt-3 mt-mb-0 wow fadeInUp" data-wow-delay=".5s">
                                The Foundation operates in the region surrounding Jacmel, Haiti’s seventh largest city.

                                Jacmel is located on Haiti’s southern most peninsula, which is separated from the
                                troubled
                                Port-au-Prince region by one of Haiti’s mountain ranges.  Thankfully, Jacmel continues
                                to
                                be peaceful, and the Foundation’s operations have remained mostly unaffected by the
                                turmoil in other parts of Haiti.


                            </p>
                            <p class="text mt-3 mt-mb-0 wow fadeInUp" data-wow-delay=".5s">
